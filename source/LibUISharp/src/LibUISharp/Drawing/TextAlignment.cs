namespace LibUISharp.Drawing
{
    /// <summary>
    /// Specifies whether text is left-aligned, right-aligned, or centered.
    /// </summary>
    public enum TextAlignment : uint
    {
        /// <summary>
        /// Text is aligned to the left.
        /// </summary>
        Left = 0,

        /// <summary>
        /// Text is centered.
        /// </summary>
        Center = 1,

        /// <summary>
        /// Text is aligned to the right.
        /// </summary>
        Right = 2
    }
}